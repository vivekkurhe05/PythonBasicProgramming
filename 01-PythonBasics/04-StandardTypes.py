# Numeric Standard Type

score = 100 # Numeric Data Type
percentage = 77.23 # Numeric Data Type

# String Standard Type

user_message = "This is my user message 777"
print(user_message.upper())

# List
# Stores multiple data of different types
# Read/Write mode
# Can insert/delete/update/fetch data
# Size will increase or decrease at runtime
li = [23,45,67.22,"Hello"]
print(li)

# Tuple
# Stores multiple data of different types
# Read only mode
# Cannot insert/delete/update, only allow to fetch data
# Size will not increase or decrease at runtime
tu = (23,45,67.22,"Hello")
print(tu)

# Dictionary
# Stores multiple data of different types
# Stores data in the form of key and value pairs
di = {"K1":"val1", "K2":"val2"}
print(di)
